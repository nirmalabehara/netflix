  function search() {
  const query = document.getElementById("searchInput").value.toLowerCase();
  const genreSections = document.querySelectorAll(".genre-section");

  genreSections.forEach(section => {
    const movieCards = section.querySelectorAll(".movie-card");
    let sectionHasMatch = false;

    movieCards.forEach(card => {
      const movieTitle = card.querySelector("p").textContent.toLowerCase();
      if (movieTitle.includes(query)) {
        card.style.display = "block";
        sectionHasMatch = true;
      } else {
        card.style.display = "none";
      }
    });

    if (!sectionHasMatch) {
      section.style.display = "none";
    } else {
      section.style.display = "block";
    }
  });
}

// Filter movies by genre
function filterMovies() {
  const selectedGenre = document.getElementById("genreSelect").value;
  const genreSections = document.querySelectorAll(".genre-section");

  genreSections.forEach(section => {
    const genre = section.getAttribute("data-genre");
    if (selectedGenre === "all" || genre === selectedGenre) {
      section.style.display = "block";
    } else {
      section.style.display = "none";
    }
  });
}

